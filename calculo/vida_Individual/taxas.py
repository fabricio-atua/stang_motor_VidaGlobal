# FUNÇÃO FAIXA ETÁRIA
# =====================================================
def faixa_etaria(idade):

    # Garante que idade não seja negativa
    idade = max(0, int(idade))

    if idade <= 19:
        return "15-19"
    elif idade <= 29:
        return "20-29"
    elif idade <= 39:
        return "30-39"
    elif idade <= 45:
        return "40-45"
    elif idade <= 49:
        return "46-49"
    elif idade <= 55:
        return "50-55"
    elif idade <= 59:
        return "56-59"
    else:
        return "60+"


